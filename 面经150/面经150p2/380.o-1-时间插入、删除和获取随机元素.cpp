class RandomizedSet {
public:
    unordered_map<int, int> hash;
    V<int> arr;
    RandomizedSet() {
        
    }
    
    bool insert(int val) {
        if (hash.contains(val)) return 0;
        hash[val] = arr.size();
        arr.pb(val);
        return 1;
    }
    
    bool remove(int val) {
        if (! hash.contains(val)) return 0;
        int idx = hash[val];
        int last = arr.back();
        hash[last] = idx;
        arr[idx] = last;
        hash.erase(val);
        arr.qb();
        return 1;
    }
    
    int getRandom() {
        int idx = rand() % arr.size();
        return arr[idx];
    }
};

