class Solution {
    public:
        int climbStairs(int n) {
            V<int> memo(n + 1, -1);
            auto dfs = [&](auto&& self, int x) {
                if (x <= 2) return x;
                int& res = memo[x];
                if (res != -1) return res;
                return res = self(self, x - 1) + self(self, x - 2);
            };
            return dfs(dfs, n);
        }
    };